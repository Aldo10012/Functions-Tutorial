def area(width, height):
    result = width * height
    return result
result = area(5, 6)
print(result)

print(area(9,3))
print(area(2,7))

def subtract(num1, num2):
    result = num1 - num2
    return result

print(subtract(7,1))
print(subtract(13,5))

def divide(num1, num2):
    result = num1 / num2
    return result

def add(num1, num2):
    result = num1 + num2
    return result